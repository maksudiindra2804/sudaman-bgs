	public function actionExport()
    {
        $model=new MappingKarier;
		$model->unsetAttributes();  // clear any default values
		if(isset($_POST['MappingKarier']))
			$model->attributes=$_POST['MappingKarier'];

		$exportType = $_POST['fileType'];
        $this->widget('ext.heart.export.EHeartExport', array(
            'title'=>'List of MappingKarier',
            'dataProvider' => $model->search(),
            'filter'=>$model,
            'grid_mode'=>'export',
            'exportType'=>$exportType,
            'columns' => array(
	                
					'id_mapping',
					'mapping',
					'child_mapping',
					'tgl_data',
	            ),
        ));
    }

    /**
	* Creates a new model.
	* If creation is successful, the browser will be redirected to the 'view' page.
	*/
	public function actionImport()
	{
		
		$model=new MappingKarier;
		// Uncomment the following line if AJAX validation is needed
		// $this->performAjaxValidation($model);

		if(isset($_POST['MappingKarier']))
		{
			if (!empty($_FILES)) {
				$tempFile = $_FILES['MappingKarier']['tmp_name']['fileImport'];
				$fileTypes = array('xls','xlsx'); // File extensions
				$fileParts = pathinfo($_FILES['MappingKarier']['name']['fileImport']);
				if (in_array(@$fileParts['extension'],$fileTypes)) {

					Yii::import('ext.heart.excel.EHeartExcel',true);
	        		EHeartExcel::init();
	        		$inputFileType = PHPExcel_IOFactory::identify($tempFile);
					$objReader = PHPExcel_IOFactory::createReader($inputFileType);
					$objPHPExcel = $objReader->load($tempFile);
					$sheetData = $objPHPExcel->getActiveSheet()->toArray(null,true,true,true);
					$baseRow = 2;
					$inserted=0;
					$read_status = false;
					while(!empty($sheetData[$baseRow]['A'])){
						$read_status = true;						
						//$id_mapping=  $sheetData[$baseRow]['A'];
						$mapping=  $sheetData[$baseRow]['B'];
						$child_mapping=  $sheetData[$baseRow]['C'];
						$tgl_data=  $sheetData[$baseRow]['D'];

						$model2=new MappingKarier;
						//$model2->id_mapping=  $id_mapping;
						$model2->mapping=  $mapping;
						$model2->child_mapping=  $child_mapping;
						$model2->tgl_data=  $tgl_data;

						try{
							if($model2->save()){
								$inserted++;
							}
						}
						catch (Exception $e){
							Yii::app()->user->setFlash('error', "{$e->getMessage()}");
							//$this->refresh();
						} 
						$baseRow++;
					}	
					Yii::app()->user->setFlash('success', ($inserted).' row inserted');	
				}	
				else
				{
					Yii::app()->user->setFlash('warning', 'Wrong file type (xlsx, xls, and ods only)');
				}
			}


			$this->render('admin',array(
				'model'=>$model,
			));
		}
		else{
			$this->render('admin',array(
				'model'=>$model,
			));
		}
	}

